<p>Плагин для копирования цен товара в цены его торговых предложений.</p>
<p>В зависимости от типа выбранного инфоблока (инфоблок с товарами или инфоблок с ТП) возможны два варианта работы:</p>
<ul>
	<li>при выборе инфоблока с товарами, для каждого найденного товара будут скопированы его цены в цены его ТП,</li>
	<li>при выборе инфоблока с ТП, для каждого найденного ТП устанавливаются цены родительского товара</li>
</ul>
