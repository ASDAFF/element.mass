<p>Плагин позволяет сделать разделы, определенные в источнике данных:
	<ul>
		<li>активными без всяких условий</li>
		<li>активными, если в разделе или в любом из его подразделах присутвуют активные элементы;</li>
		<li>не активными без всяких условий</li>
		<li>не активными, если в разделе и в любом из его подразделах отсутвуют активные элементы.</li>
	</ul>
</p>